#Evaluates True

if ""
  puts "the empty string is true"
end

if "false"
  puts "the false string is true"
end

if 0
  puts "the number 0 is true"
end

if 1
  puts "the number 1 is true"
end

if []
  puts "the empty array is true"
end

#Evaluates False

if false
  puts "the value false is true"
end

if nil
  puts "nil is true"
end